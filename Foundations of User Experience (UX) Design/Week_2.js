//Universal design, inclusive design, and equity-focused design
/*
    - Accessibility
    - Universal design - One size fits all
    - Inclusive design - Solve for one, extend to many
        - Ability
        - Race
        - Economic status
        - Language
        - Age
        - Gender
    - Equity-focused design - Thinking through all the aspects
        - Equality - providing the same amount of oppurtunity and support
        - Equity - providing different levels of oppurtunity and support for each
        person in order to achieve fair outcomes
*/

